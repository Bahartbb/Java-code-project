package com.bahar;

public class NegationOfNumbers {
    //This task deals with the negation of numbers. The aim of the task is to change the sign of a given number.

    public static void main(String[] args) {

        System.out.println(negation(2));
        System.out.println(negation(7));
        System.out.println(negation(100));

    }

    public static int negation(int num){
        return -num;
    }



}
